package com.appearnetworks.aiq.ia.dataaccess.model;


public class SubCode2DO {
    private String id;
    private String name;

    public SubCode2DO(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
